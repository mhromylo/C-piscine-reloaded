int	ft_sqrt(int nb)
{
	int	a;

	a = 1;
	if (nb < 0)
		return (0);
	else
	{
		while ((a * a <= nb) && (a <= 46340))
		{
			if (a * a == nb)
				return (a);
			else
				a++;
		}
		return (0);
	}
}
